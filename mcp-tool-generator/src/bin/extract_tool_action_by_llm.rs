use mcp_server::config::tool::{ToolAction, ToolDef, ToolFile, ToolFileConfig};
use mcp_tool_generator::llm_client::LlmClient;
use async_openai::types::chat::{
    ChatCompletionRequestMessage, ChatCompletionRequestSystemMessageArgs,
    ChatCompletionRequestUserMessageArgs,
};

/// 读取 res/cargo_build_help，让 LLM 提取 ToolAction::Command 相关信息（command, args, sub_dir），
/// 然后序列化为 ToolFile 并写入文件。
#[tokio::main]
async fn main() -> anyhow::Result<()> {
    custom_utils::logger::logger_stdout_debug();

    let base_url = std::env::var("LLM_BASE_URL").unwrap_or_else(|_| "http://127.0.0.1:8082/v1".to_string());
    let model = std::env::var("LLM_MODEL").unwrap_or_else(|_| "Intel/Qwen3.5-122B-A10B-int4-AutoRound".to_string());
    let help_path = std::env::args().nth(1).unwrap_or_else(|| "./res/cargo_build_help".to_string());
    let output_path = std::env::args().nth(2).unwrap_or_else(|| "./tools.d/cargo_build_llm.toml".to_string());

    println!("==> 读取 help 文件: {}", help_path);
    let help_text = tokio::fs::read_to_string(&help_path).await?;

    let llm = LlmClient::new(&base_url, &model);

    let system = r#"你是一个 CLI 命令分析器。根据给定的命令 --help 输出，提取以下信息并以 JSON 格式返回：

1. command: 要执行的命令（字符串），例如 "cargo"
2. args: 固定参数列表（字符串数组），例如 ["build"]
3. sub_dir: 如果命令需要在特定子目录下运行，指定子目录路径（字符串或 null）
4. name: tool 的名称（用下划线连接的命令名），例如 "cargo_build"
5. description: tool 的描述（从 help 输出的第一行提取）
6. parameters: 从 help 输出中提取的参数列表，每个参数包含：
   - name: 参数名（使用下划线命名，不含 -- 前缀）
   - description: 参数描述
   - type: 参数类型（"string", "boolean", "array"）
   - required: 是否必需（布尔值）

输出格式（纯 JSON，不要 markdown）：
{
  "command": "...",
  "args": ["..."],
  "sub_dir": null,
  "name": "...",
  "description": "...",
  "parameters": [
    {"name": "...", "description": "...", "type": "...", "required": false}
  ]
}
"#;

    let user = format!("请分析以下命令的 --help 输出，提取 ToolAction::Command 相关信息：\n\n{}", help_text);

    let messages: Vec<ChatCompletionRequestMessage> = vec![
        ChatCompletionRequestSystemMessageArgs::default()
            .content(system)
            .build()
            .unwrap()
            .into(),
        ChatCompletionRequestUserMessageArgs::default()
            .content(user)
            .build()
            .unwrap()
            .into(),
    ];

    println!("==> 正在调用 LLM ({})...\n", model);
    let response = llm.chat(messages).await?;
    println!("==> LLM 返回:\n{}\n", response);

    // 解析 JSON 响应
    let json_str = extract_json(&response);
    let parsed: serde_json::Value = serde_json::from_str(json_str)?;

    let command = parsed["command"].as_str().unwrap_or("cargo").to_string();
    let args: Option<Vec<String>> = parsed["args"]
        .as_array()
        .map(|a| a.iter().filter_map(|v| v.as_str().map(String::from)).collect());
    let sub_dir = parsed["sub_dir"].as_str().map(String::from);
    let name = parsed["name"].as_str().unwrap_or("cargo_build").to_string();
    let description = parsed["description"].as_str().unwrap_or("").to_string();

    let parameters = parsed["parameters"]
        .as_array()
        .map(|arr| {
            arr.iter()
                .filter_map(|p| {
                    Some(mcp_server::config::tool::ParameterDef {
                        name: p["name"].as_str()?.to_string(),
                        description: p["description"].as_str().unwrap_or("").to_string(),
                        r#type: p["type"].as_str().unwrap_or("string").to_string(),
                        required: p["required"].as_bool().unwrap_or(false),
                        arg: None,
                    })
                })
                .collect()
        });

    let tool_def = ToolDef {
        name,
        description,
        action: ToolAction::Command {
            command: Some(command),
            args,
            sub_dir,
        },
        env: None,
        timeout_secs: None,
        parameters,
    };

    let tool_file = ToolFile {
        config: Some(ToolFileConfig {
            working_dir: Some(".".to_string()),
            timeout_secs: None,
            env: None,
            base_url: None,
        }),
        tools: vec![tool_def],
    };

    let toml_str = toml::to_string_pretty(&tool_file)?;
    let output = format!(
        "# Auto-generated tool configuration for: cargo build\n# Generated by extract_tool_action_by_llm via LLM\n\n{}",
        toml_str
    );

    println!("==> 生成的 TOML:\n{}\n", output);
    tokio::fs::write(&output_path, &output).await?;
    println!("==> 已写入: {}", output_path);

    Ok(())
}

fn extract_json(response: &str) -> &str {
    let trimmed = response.trim();
    // Try markdown code block
    if let Some(start) = trimmed.find("```json") {
        let after = &trimmed[start + 7..];
        if let Some(end) = after.find("```") {
            return after[..end].trim();
        }
    }
    if let Some(start) = trimmed.find("```") {
        let after = &trimmed[start + 3..];
        // skip optional language tag on first line
        let after = if let Some(nl) = after.find('\n') {
            &after[nl + 1..]
        } else {
            after
        };
        if let Some(end) = after.find("```") {
            return after[..end].trim();
        }
    }
    // Try raw JSON
    if let Some(start) = trimmed.find('{') {
        if let Some(end) = trimmed.rfind('}') {
            return &trimmed[start..=end];
        }
    }
    trimmed
}
